package thread.wang_bao_ling.c16;

public class Connect {
    private int id;
    private String name;

    public Connect(){}

    public Connect(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
